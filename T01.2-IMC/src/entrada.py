def lee_entero(mensaje):
    """
    Toma una cadena de caracteres como entrada que representa un mensaje
    que se muestra al usuario
    Espera a que el ususario introduzca un dato por la consola 
    y que devuelve ese dato como entero
    """
    return int(input(mensaje))

